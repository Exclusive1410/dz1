class product:
    def __init__(self, keyboard, price):
        if keyboard <= 0:
            raise ValueError('You didnt order anything')
        self.keyboard = keyboard
        self.price = price

def _add_price(self):
    return self.price - (price/100*20)

def __get_price(self):
    if self.__add_price(self) == 10:
        return self.keyboard - 1
    return self.keyboard

def __str__(self):
    return f'Price of products: ${self.__get_price()}'